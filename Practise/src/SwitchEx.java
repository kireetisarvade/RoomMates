
public class SwitchEx
{
 	public static void main(String[] args)
	{
		String str ="sushi";
		
		switch (str)
		{
		case 
		"mahi":System.out.println("case one executed");
		break;
		case 
		"sushi":System.out.println("case two executed");
		break;
		case
		"gani":System.out.println("case 3 executed");
		default:
			break;
		}
	}

}
